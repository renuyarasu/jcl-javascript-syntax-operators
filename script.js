// JavaScript Syntax and Operators
console.clear();

// Math Operators: Assignment Operators
function assignmentOperators() {
    let price = 100;
    console.log('price = ' + price.toString());

    // Addition
    price += 100;
    console.log('price += 100 = ' + price.toString());// price += 100 = 200

    // Subtraction
    price -= 100;
    console.log('price -= 100 = ' + price.toString());// price += 100 = 100

    // Multiplication
    price *= 10;
    console.log('price *= 10 = ' + price.toString()); // price *= 10 = 1000

    // Division
    price /= 10;
    console.log('price /= 10 = ' + price.toString()); // price /= 10 = 100

    // Exponentiation
    price **= 2;
    console.log('price **= 2 = ' + price.toString()); // price **= 2 = 10000

    //Modules
    price %= 5;
    console.log('price %= 5 = ' + price.toString()); // price %= 5 = 0

}







