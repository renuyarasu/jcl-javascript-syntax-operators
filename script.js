console.clear();

function simpleSwitch() {
    let productId = 2;
    switch (productId) {
        case 1:
            console.log('Product 1');
            break;

        case 2:
            console.log('Product 2');
            break;

        case 3:
            console.log('Product 3');
            break;
        default:
            console.log('No products available');
    }
}